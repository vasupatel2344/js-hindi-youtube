const month = 3

switch (month) {
    case 1:
        console.log("January 1");
        break;

    case 2:
        console.log("February 2");
        break;

    case 3:
        console.log("March 3");
        break;

    case 4:
        console.log("April 4");
        break;

    default:
        console.log("Default case march");
        break;
}


















